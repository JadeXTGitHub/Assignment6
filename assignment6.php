<p>A Flexible Layout must have a parent element with the <em>display</em> property set to <em>flex</em>.</p>
<p>The Flexible Box Layout Module, makes it easier to design flexible responsive layout structure without using float or positioning.</p>
<p>Direct child elements(s) of the flexible container automatically becomes flexible items.</p>
<p>The webpage I used to learn more about CSS page layout techniques is <a href="https://www.w3schools.com/css/css3_flexbox.asp"> W3 Schools</a> </p>
<p>The CSS Flexbox Layout design technique follows the principals of design technique:</p>

<li>Contrast - Flexbox layout allows for any colors to be used to create contrast</li>
<li>Repetition - Flexbox layout allows for repetition and consistancy</li>
<li>Alignment - Flexbox layout allows for box like alignment</li>
<li>Proximity - Flexbox layout allows for spacing of boxes</li>
